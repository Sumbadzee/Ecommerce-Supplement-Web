const languageSelector = document.querySelector("select");

languageSelector.addEventListener("change", (event) => {
    setLanguage(event.target.value);
});

const setLanguage = (language) => {
    const titleElement = document.querySelector("h2");
    const productCards = document.querySelectorAll(".product-card");

    if (translations[language]) {
        titleElement.textContent = translations[language].title;

        productCards.forEach((card, index) => {
            if (translations[language].products[index]) {
                const nameElement = card.querySelector("h3");
                const priceElement = card.querySelector(".price");
                const ratingElement = card.querySelector(".rating");
                const buttonElement = card.querySelector("button");

                nameElement.textContent = translations[language].products[index].name;
                priceElement.textContent = translations[language].products[index].price;
                ratingElement.textContent = translations[language].products[index].rating;
                buttonElement.textContent = translations[language].products[index].button;
            }
        });

    }
};
